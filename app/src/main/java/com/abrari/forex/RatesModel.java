package com.abrari.forex;

public class RatesModel {
    private double AED, AFN, ALL, AMD, ARS, BRL, HKD, INR, JPY, RUB;

    public RatesModel(){}

    public double getAED() {return AED;}

    public void setAED(double AED) {this.AED = AED;}

    public double getAFN() {return AFN;}

    public void setAFN(double AFN) {this.AFN = AFN;}

    public double getALL() {return ALL;}

    public void setALL(double ALL) {this.ALL = ALL;}

    public double getAMD() {return AMD;}

    public void setAMD(double AMD) {this.AMD = AMD;}

    public double getARS() {return ARS;}

    public void setARS(double ARS) {this.ARS = ARS;}

    public double getBRL() {return BRL;}

    public void setBRL(double BRL) {this.BRL = BRL;}

    public double getHKD() {return HKD;}

    public void setHKD(double HKD) {this.HKD = HKD;}

    public double getINR() {return INR;}

    public void setINR(double INR) {this.INR = INR;}

    public double getJPY() {return JPY;}

    public void setJPY(double JPY) {this.JPY = JPY;}

    public double getRUB() {return RUB;}

    public void setRUB(double RUB) {this.RUB = RUB;}
}
